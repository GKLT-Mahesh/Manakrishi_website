function setLanguage(lang) {
    localStorage.setItem('manakrishi_lang', lang);
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    // Update button Text
    const langBtnText = document.getElementById('lang-btn-text');
    if (langBtnText) {
        langBtnText.innerText = lang === 'en' ? 'తెలుగు' : 'English';
    }
}

function toggleLanguage() {
    const currentLang = localStorage.getItem('manakrishi_lang') || 'en';
    const newLang = currentLang === 'en' ? 'te' : 'en';
    setLanguage(newLang);
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('manakrishi_lang') || 'en';
    setLanguage(savedLang);
});
